$().ready(function () {
	$("#loginForm").validate({
		rules:{
			userCode:{
				required:true,
				minlength:2,
				maxlength:30
			},
			pass:{
				required:true,
				minlength:3,
				maxlength:12
			}
		},
		messages:{
			userCode:{
				required:"请输入账号",
				minlength:"账号长度不能小于6位",
				maxlength:"账号长度不能超过30位"
			},
			pass:{
				required:"请输入密码",
				minlength:"密码长度不能小于6位",
				maxlength:"密码长度不能超过12位"
			}
		},
		submitHandler:function(form){
            form.submit();
        } 
	});
});
